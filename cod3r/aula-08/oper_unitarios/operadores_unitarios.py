"""
OPERADOR UNITÁRIO

NOT = Podemos considerar como o != no Javascript
- MENOS
+ MAIS
"""

# Não FALSE, ou seja, o contrário de False
print(not False)

# NÃO TRUE, ou seja, o contrário de Verdadeiro
print(not True)

y = 6

print(f"Retornando um valor negativo: {-4}.\n")
print(f"Retornando o valor da variável 'y' (4) de maneira que seja negativo: {-y}.\n")
print(f"Se passarmos um valor que é antecido por dois '-' (menos), ele se tornará positivo.\nExemplo: --2 => {--2}.\n")
print(f"Se passarmos um valor que é antecido por + '+' (mais), ele retornará, de fato o número positivo.\nExemplo: +10 => {+10}.\n")