"""
OPERADORES RELACIONAIS

< Maior
> Menor
== Igual
!= Diferente

"""

print("==== Operadores Relacionais ====")

x = 7
y = 5
z = 7

print(f"Criamos três variáveis, para nos ajudarem a compreender:\n- x = {x};\n- y = {y};\n- z = {z}.\n")

print(f"X é menor que Y? => { x < y }.\n")
print(f"X é menor ou igual a Y? => { x <= y }.\n")
print(f"X é igual a Z? => { x == z }.\n")
print(f"X é maior que Y? => { x > y }.\n")
print(f"X é maior ou igual a Y? => { x >= y }.\n")
print(f"X é diferente de Y? => { x != y }.\n")
print(f"5 é igual a '5'? => { 5 == '5' }.\n")